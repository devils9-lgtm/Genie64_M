        // =========================================================================================
        // [만능 헬퍼] 목표 수량 미달 시 현금 대체 매도! (전략2 즉시매도 + 키움 소급이자 회피 정렬 엔진)
        // =========================================================================================
        public static void 신용주문_분할매도_실행(Stockbalance 잔고, int 총_매도목표수량, Action<bool, string, int> 주문실행_콜백)
        {
            if (잔고 == null || 총_매도목표수량 <= 0) return;

            int 남은주문수량 = 총_매도목표수량;

            double 적용세율 = (잔고.시장 == "E") ? 0.0 : Form1.TAX;
            double 적용수수료 = Form1.수수료;

            // [+] 아빠님이 외부에서 조절할 수 있도록 변수 처리
            double 정렬_수익률그룹단위 = 0.5;

            // [신규 추가] 긴급 탈출 조건 변수
            double 긴급_수익률조건 = 0.3;
            int 긴급_이자일수조건 = 2; // 이자 폭등까지 2일 이하로 남았을 때

            // =========================================================================
            // 1. [신용 매도] 0순위 긴급탈출 -> 0.5% 단위 그룹화 -> 이자 임박 -> 실제수익률
            // =========================================================================
            double 신용총매입금액 = 0;

            if (잔고.신용상세리스트 != null && 잔고.신용상세리스트.Count > 0)
            {
                DateTime 기준오늘 = DateTime.Today;

                var 정렬된_신용리스트 = 잔고.신용상세리스트.Select(item =>
                {
                    double 계산된수익률 = 0;
                    int 이자구간남은일수 = 999;
                  //  int 만기남은일수 = 999;

                    // 1) 실제 제비용 차감 수익률 계산
                    if (item.매입가 > 0 && item.보유수량 > 0)
                    {
                        double 개별매입금액 = item.매입가 * item.보유수량;
                        double 개별평가금액 = 잔고.현재가 * item.보유수량;

                        double 매수수수료금액 = 개별매입금액 * 적용수수료;
                        double 매도수수료금액 = 개별평가금액 * 적용수수료;
                        double 세금금액 = 개별평가금액 * 적용세율;
                        double 신용이자금액 = item.신용이자;

                        double 제비용합계 = 매수수수료금액 + 매도수수료금액 + 세금금액 + 신용이자금액;
                        double 실제순수익금 = 개별평가금액 - 개별매입금액 - 제비용합계;
                        계산된수익률 = (실제순수익금 / 개별매입금액) * 100.0;
                    }

                    // 2) 키움증권 소급방식 이자율 절벽 구간 임박 계산
                    if (DateTime.TryParseExact(item.대출일, "yyyyMMdd", System.Globalization.CultureInfo.InvariantCulture, System.Globalization.DateTimeStyles.None, out DateTime 대출일자))
                    {
                        int 경과일수 = (int)(기준오늘 - 대출일자).TotalDays;

                        if (경과일수 < 7) 이자구간남은일수 = 7 - 경과일수;
                        else if (경과일수 < 15) 이자구간남은일수 = 15 - 경과일수;
                        else if (경과일수 < 90) 이자구간남은일수 = 90 - 경과일수;
                    }

                    // 3) 만기일 임박 계산 (현재 주석/보류 상태라면 생략 가능)

                    // [+] 핵심: 0순위 긴급 매도 대상인지 판별 (수익률 0.3% 이상 AND 이자 폭등 2일 이하)
                    bool 긴급매도대상 = (계산된수익률 >= 긴급_수익률조건 && 이자구간남은일수 <= 긴급_이자일수조건);

                    return new
                    {
                        원본객체 = item,
                        실제수익률 = 계산된수익률,
                        그룹화수익률 = Math.Floor(계산된수익률 / 정렬_수익률그룹단위) * 정렬_수익률그룹단위,
                        이자구간남은일수 = 이자구간남은일수,
                        긴급매도대상 = 긴급매도대상 // 새롭게 추가된 판별값
                    };
                })
                // -----------------------------------------------------------------------------------------
                // [+] 5단계 정렬 엔진 (아빠님의 세분화 아이디어 완벽 적용)
                // -----------------------------------------------------------------------------------------
                .OrderByDescending(x => x.긴급매도대상)      // 0순위: 조건(0.3% 이상 + 이자 2일 이하) 만족 시 무조건 1등 프리패스
                .ThenByDescending(x => x.그룹화수익률)      // 1순위: 0.5% 단위 수익률 바구니
                .ThenBy(x => x.이자구간남은일수)             // 2순위: 바구니 내에서 이자 폭등 남은 일수 (오름차순)
                .ThenByDescending(x => x.실제수익률)         // 3순위: 바구니 내에서 실제 수익률 (내림차순)
                .ToList();

                foreach (var data in 정렬된_신용리스트)
                {
                    var item = data.원본객체;

                    if (남은주문수량 <= 0) break;
                    if (item.보유수량 <= 0) continue;

                    신용총매입금액 += (item.매입가 * item.보유수량);

                    int 이번에_팔_수량 = Math.Min(남은주문수량, item.보유수량);

                    item.보유수량 -= 이번에_팔_수량;
                    남은주문수량 -= 이번에_팔_수량;

                    주문실행_콜백(true, item.대출일, 이번에_팔_수량);
                    홀딩잔고.주문가능수업데이트(잔고, "매도", 이번에_팔_수량, "매도주문", true);
                }
            }

            // =========================================================================
            // 2. [현금 매도] 신용을 다 털고 남은 목표 수량을 현금 잔고에서 마저 텁니다!
            // =========================================================================
            if (남은주문수량 > 0 && GET.총주문가능수량(잔고) > 0)
            {
                int 현금매도_수량 = Math.Min(남은주문수량, 잔고.주문가능수량);

                bool 현금매도_승인 = true;

                // 현금 주문 실행 
                if (현금매도_승인)
                {
                    주문실행_콜백(false, "", 현금매도_수량);
                    홀딩잔고.주문가능수업데이트(잔고, "매도", 현금매도_수량, "매도주문", false);

                    남은주문수량 -= 현금매도_수량;
                }
            }

            // =========================================================================
            // 3. [안전장치] 남은 수량 보고
            // =========================================================================
            if (남은주문수량 > 0)
            {
                Form1.Console_print($"[-] {잔고.종목명}: 계좌 잔고(주문가능수량) 부족으로 인해 {남은주문수량}주가 매도되지 못했습니다.");
            }
        }

        // --------------------------------------------------------------------------------
        // [함수] 현금+신용 개별 합산(HTS 방식) 실시간 실질 수익률 및 '실질 순손익' 계산
        // --------------------------------------------------------------------------------
        public static double Get_Real_Profit_Rate(Stockbalance 잔고, out long 실질순손익)
        {
            실질순손익 = 0;

            // ==========================================================
            // [방어막 1] 데이터 찢어짐(비동기 엇박자) 현상 전량 매도 차단
            // ==========================================================
            long 총보유수량 = 잔고.보유수량 + 잔고.신용_보유수량;
            long 총매입금액_확인용 = 잔고.매입금액 + 잔고.신용_매입금액;

            if (총보유수량 == 0 && 총매입금액_확인용 > 0)
            {
                return 0.0;
            }

            // ==========================================================
            // [방어막 2] 현금 잔고 비동기 엇박자 감지 (현금 데이터만 비교)
            // ==========================================================
            if (잔고.보유수량 > 0 && 잔고.평균단가 > 0)
            {
                long 예상_현금매입금액 = 잔고.보유수량 * 잔고.평균단가;

                if (잔고.매입금액 > 0 && Math.Abs(잔고.매입금액 - 예상_현금매입금액) > (예상_현금매입금액 * 0.03))
                {
                    실질순손익 = 잔고.평가손익;
                    return 잔고.수익률;
                }
            }

            // ==========================================================
            // [방어막 3] 신용상세리스트 갱신 딜레이(엇박자) 완벽 차단
            // ==========================================================
            if (잔고.신용상세리스트 != null && 잔고.신용상세리스트.Count > 0)
            {
                long 상세리스트_합산수량 = 0;
                foreach (var 상세 in 잔고.신용상세리스트)
                {
                    if (상세.보유수량 > 0) 상세리스트_합산수량 += 상세.보유수량;
                }

                if (상세리스트_합산수량 != 잔고.신용_보유수량)
                {
                    실질순손익 = 잔고.평가손익;
                    return 잔고.수익률;
                }
            }

            // ==========================================================
            // 1. [총 매입금액 확인 및 0 나누기 방지]
            // ==========================================================
            long 총매입금액 = 잔고.매입금액 + 잔고.신용_매입금액;

            if (총매입금액 == 0)
            {
                if ((잔고.평가금액 + 잔고.신용_평가금액) > 0) return 100.0;
                return 0.0;
            }

            long 실시간현재가 = 잔고.현재가;
            double tax_rate = (잔고.시장 == "E") ? 0 : Form1.TAX;
            long 누적_순손익 = 0;

            // ==========================================================
            // 2. [현금 잔고 정밀 계산]
            // ==========================================================
            if (잔고.보유수량 > 0)
            {
                long 현금_평가금액 = 실시간현재가 * 잔고.보유수량;

                long 현금_매수수수료 = (long)((잔고.매입금액 * Form1.수수료) / 10) * 10;
                long 현금_매도수수료 = (long)((현금_평가금액 * Form1.수수료) / 10) * 10;
                long 현금_세금 = (long)(현금_평가금액 * tax_rate);

                long 현금_순손익 = (현금_평가금액 - 잔고.매입금액) - 현금_매수수수료 - 현금_매도수수료 - 현금_세금;
                누적_순손익 += 현금_순손익;
            }

            // ==========================================================
            // 3. [신용 잔고 정밀 계산] - 개별 물량별(Row별) HTS 합산 방식
            // ==========================================================
            if (잔고.신용상세리스트 != null && 잔고.신용상세리스트.Count > 0)
            {
                long 신용_총평가금액 = 0;
                long 신용_누적_수수료및이자 = 0;
                long 신용_총매입금액 = 0;

                foreach (var 상세 in 잔고.신용상세리스트)
                {
                    if (상세.보유수량 <= 0) continue;

                    long 항목_평가금액 = 실시간현재가 * 상세.보유수량;

                    long 항목_매수수수료 = (long)((상세.매입금액 * Form1.수수료) / 10) * 10;
                    long 항목_매도수수료 = (long)((항목_평가금액 * Form1.수수료) / 10) * 10;

                    신용_총평가금액 += 항목_평가금액;
                    신용_총매입금액 += 상세.매입금액;
                    신용_누적_수수료및이자 += (항목_매수수수료 + 항목_매도수수료 + 상세.신용이자);
                }

                // 세금은 개별 절사하면 HTS 원단위와 달라질 수 있으므로 총액 기준으로 한 번만 계산
                long 신용_총세금 = (long)(신용_총평가금액 * tax_rate);
                long 신용_총순손익 = (신용_총평가금액 - 신용_총매입금액) - 신용_누적_수수료및이자 - 신용_총세금;

                누적_순손익 += 신용_총순손익;
            }

            // ==========================================================
            // 4. [최종 결과 도출]
            // ==========================================================
            실질순손익 = 누적_순손익;

            double real_rate = ((double)누적_순손익 / 총매입금액) * 100.0;

            // HTS와 완벽히 동일한 일반 반올림(사사오입) 적용
            return Math.Round(real_rate, 2, MidpointRounding.AwayFromZero);
        }


        public static long Get_Estimated_Max_Buying_Power()
        {
            if (!GenieConfig.CB_신용_주문사용) return Math.Max(0, Form1.Acc.추정D2);

            double 종목증거금률 = 0.45; // 키움 표준
            double 안전비율 = Math.Max(0, (double)GenieConfig.TB_신용_신용증거금비율);

            // 1. 자산 파악
            long 총_현금매입금 = 0;
            long 총_ETF매입금 = 0;
            long 총_신용매입금 = 0;

            foreach (var 잔고 in Form1.stockBalanceList.Values)
            {
                if (잔고.시장 == "E") 총_ETF매입금 += 잔고.매입금액;
                else 총_현금매입금 += 잔고.매입금액;

                총_신용매입금 += 잔고.신용_매입금액;
            }

            // ==========================================================
            // 🚀 [추가] 20억 절대 한도 파악
            // ==========================================================
            long 키움_개인한도_최대치 = 2000000000;
            long 남은_절대한도 = 키움_개인한도_최대치 - 총_신용매입금;
            if (남은_절대한도 < 0) 남은_절대한도 = 0;

            long 사용중인_신용담보 = (long)(총_신용매입금 * 종목증거금률);
            long 내현금 = Form1.Acc.추정D2;
            if (내현금 < 0) 내현금 = 0; // 안전장치

            // 2. 진짜 내 총 원금 (모든 자산 합계)
            long 내_진짜원금 = 총_현금매입금 + 총_ETF매입금 + 내현금 + 사용중인_신용담보;

            // 3. 내 전체 돈 중에서 '신용 담보(증거금)'로 써도 되는 총 한도 계산
            long 전체_담보_허용치 = (long)(내_진짜원금 * (1.0 - (안전비율 / 100.0)));

            // 4. 남은 담보 여력
            long 남은_담보여력 = 전체_담보_허용치 - 사용중인_신용담보;
            if (남은_담보여력 < 0) 남은_담보여력 = 0;

            // 5. 신용에 '쓸 수 있도록 허락된' 가용현금
            long 진짜_가용현금 = Math.Min(내현금, 남은_담보여력);

            // ==========================================================
            // 🚀 [핵심 로직 변경] 20억 리밋 + 남은 현금 하이브리드 합산
            // ==========================================================

            // A. 이론상 내 담보여력으로 뽑아낼 수 있는 최대 신용 매수액
            long 이론상_신용가능액 = (long)(진짜_가용현금 / 종목증거금률);

            // B. 하지만 키움 한도 20억에 막힌다면? 둘 중 작은 값이 '진짜 신용 매수액'
            long 실제_신용가능액 = Math.Min(이론상_신용가능액, 남은_절대한도);

            // C. 위 B에서 확정된 신용 매수를 하기 위해 증권사에 묶이는 내 현금(증거금)
            long 소모된_증거금 = (long)(실제_신용가능액 * 종목증거금률);

            // D. 내 전체 현금 중에서 증거금으로 묶이고 남은 잉여 현금 (이건 현금매수에 씁니다!)
            long 남은_순수현금 = 내현금 - 소모된_증거금;
            if (남은_순수현금 < 0) 남은_순수현금 = 0;

            // E. 최종 한도 = (신용으로 뻥튀기한 금액) + (남은 잉여 현금 1:1)
            // 기존처럼 수수료/시장변동성 대비 0.98 버퍼 곱해주기
            long 최종한도 = (long)((실제_신용가능액 + 남은_순수현금) * 0.98);

            // ==========================================================

            if (최종한도 < 0)
            {
                return Math.Max(0, Form1.Acc.추정D2);
            }

            return 최종한도;
        }

        public static bool 신용주문_해야하나(int 매수매도, int 주문수량, Market_Item item, Stockbalance 잔고, out int 실제주문할수량)
        {
            // [로그 스위치] 디버깅이 필요할 때만 true로 변경하세요.
            bool 로그출력 = false;

            List<string> logBuffer = new List<string>();
            logBuffer.Add($"\n--- [주문검토 시작] {item.종목명}({item.종목코드}) / 목표수량: {주문수량} ---");
            실제주문할수량 = 0;

            if (매수매도 == 1)
            {
                if (GenieConfig.CB_신용_주문사용 == false)
                {
                    실제주문할수량 = 주문수량;
                    return false;
                }
            }
            else if (매수매도 == 2) // 매도는 즉시 통과
            {
                실제주문할수량 = 주문수량;
                if (로그출력) { foreach (var log in logBuffer) Form1.Console_print(log); }
                return true;
            }

            // ==========================================================================
            // 1. [핵심 교체] 유동적 역산 공식 (0원 버그 해결판)
            // ==========================================================================
            long 총_현금매입금 = 0;
            long 총_ETF매입금 = 0;
            long 총_신용매입금 = 0;

            // [개선] 전체 계좌의 '평균적인' 과거 신용 증거금률 (대략 45%로 가정)
            double 계좌평균_증거금비율 = 0.45;

            foreach (var 잔 in Form1.stockBalanceList.Values)
            {
                if (잔.시장 == "E") 총_ETF매입금 += 잔.매입금액;
                else 총_현금매입금 += 잔.매입금액;

                총_신용매입금 += 잔.신용_매입금액;
            }

            long 사용중인_신용담보 = (long)(총_신용매입금 * 계좌평균_증거금비율);
            long 내현금 = Form1.Acc.추정D2;

            long 내_진짜원금 = 총_현금매입금 + 총_ETF매입금 + 내현금 + 사용중인_신용담보;

            double 안전비율 = Math.Max(0, (double)GenieConfig.TB_신용_신용증거금비율);
            long 전체_담보_허용치 = (long)(내_진짜원금 * (1.0 - (안전비율 / 100.0)));

            long 남은_담보여력 = 전체_담보_허용치 - 사용중인_신용담보;
            if (남은_담보여력 < 0) 남은_담보여력 = 0;

            long 진짜_가용현금 = Math.Min(내현금, 남은_담보여력);

            logBuffer.Add($"[자산분석] 원금:{내_진짜원금:N0} | 담보(허용:{전체_담보_허용치:N0}/사용:{사용중인_신용담보:N0}) | 가용현금:{진짜_가용현금:N0}");


            // ==========================================================================
            // 🚀 [신규 추가] 1-2. 키움증권 1인당 신용 한도(20억) 절대 방어선
            // ==========================================================================
            // 보수적 접근: 순수 대출금액이 아닌 '총 신용매입금'을 기준으로 20억을 잡습니다.
            long 키움_개인한도_최대치 = 2000000000; // 20억 원
            long 남은_절대한도 = 키움_개인한도_최대치 - 총_신용매입금;

            if (남은_절대한도 <= 0)
            {
                logBuffer.Add($"[한도초과] 키움증권 신용한도 20억 도달 (현재사용:{총_신용매입금:N0}원) -> 현금 주문으로 강제 전환!");
                return ETF포함_현금으로_계산_개선(주문수량, item.현재가, 진짜_가용현금, out 실제주문할수량, logBuffer, 로그출력);
            }
            // ==========================================================================


            // ==========================================================================
            // 2. [기본 필터링]
            // ==========================================================================
            if (item.state != null && !item.state.Contains("신용가능") || !item.신용가능 || item.증거금률 <= 0 || item.증거금률 >= 1.0 || !GenieConfig.CB_신용_주문사용)
            {
                logBuffer.Add($"[우회] 신용불가 종목(증거금률:{item.증거금률}) -> 현금주문 전환");
                return ETF포함_현금으로_계산_개선(주문수량, item.현재가, 진짜_가용현금, out 실제주문할수량, logBuffer, 로그출력);
            }

            // ==========================================================================
            // 3. [전략 판단]
            // ==========================================================================
            bool 신용사용_결정 = false;
            bool 현금대체_금지 = false;

            if (GenieConfig.CB_신용_현신별도사용) { 신용사용_결정 = (잔고?.신용_보유수량 != 0); 현금대체_금지 = 신용사용_결정; }
            else if (GenieConfig.CB_신용_신용우선사용) { 신용사용_결정 = true; 현금대체_금지 = true; }
            else if (GenieConfig.CB_신용_현금우선사용)
            {
                long 예상_현금주문액 = (long)((long)주문수량 * item.현재가 * 1.02);
                신용사용_결정 = (진짜_가용현금 < 예상_현금주문액);
            }

            // ==========================================================================
            // 4. [최종 수량 확정] - 보수적 증거금 계산 (키움 서버 에러 방지)
            // ==========================================================================
            if (신용사용_결정)
            {
                int 신용_최대수량 = 0;
                long 안전_가용현금 = 진짜_가용현금 - 20000;

                if (안전_가용현금 > 0)
                {
                    double 실전_신용증거금률 = Math.Max(0.45, item.증거금률 + 0.05);
                    long 이종목_신용가능금액 = (long)(안전_가용현금 / 실전_신용증거금률);

                    // 🚀 [신규 추가] 알고리즘상 담보 여력이 넘쳐나도, 증권사의 '20억 남은 한도'를 초과해서 살 수 없음!
                    이종목_신용가능금액 = Math.Min(이종목_신용가능금액, 남은_절대한도);

                    신용_최대수량 = (int)(이종목_신용가능금액 / item.현재가);
                }

                if (신용_최대수량 < 1)
                {
                    if (현금대체_금지)
                    {
                        logBuffer.Add("[중단] 담보한도 초과 (안전버퍼 또는 20억 절대한도 초과로 신용가능수량 0)");
                        if (로그출력) { foreach (var log in logBuffer) Form1.Console_print(log); }
                        return false;
                    }
                    return ETF포함_현금으로_계산_개선(주문수량, item.현재가, 진짜_가용현금, out 실제주문할수량, logBuffer, 로그출력);
                }

                실제주문할수량 = Math.Min(주문수량, 신용_최대수량);

                logBuffer.Add($"[성공] 신용확정(가용현금:{진짜_가용현금:N0}원 / 남은20억한도:{남은_절대한도:N0}원 / 적용증거금률:{Math.Max(0.45, item.증거금률 + 0.05):P0})");
                logBuffer.Add($"       ▶ 목표:{주문수량}주 / 최대가능:{신용_최대수량}주 -> 최종:{실제주문할수량}주 주문");

                if (로그출력) { foreach (var log in logBuffer) Form1.Console_print(log); }
                return true;
            }
            else
            {
                return ETF포함_현금으로_계산_개선(주문수량, item.현재가, 진짜_가용현금, out 실제주문할수량, logBuffer, 로그출력);
            }
        }

        // 🌟 개선된 현금 계산 보조 함수 (파라미터에 로그출력 여부 추가)
        private static bool ETF포함_현금으로_계산_개선(int 목표, int 현재가, long 가용현금, out int 결과, List<string> logBuffer, bool 로그출력)
        {
            결과 = 0;
            if (가용현금 <= 0 || 현재가 <= 0) return false;

            int 최대가능 = (int)((가용현금 * 0.98) / 현재가);
            결과 = Math.Min(목표, 최대가능);

            logBuffer.Add($"[현금주문] 가용:{가용현금:N0} -> 최종주문:{결과}");

            if (로그출력) { foreach (var log in logBuffer) Form1.Console_print(log); }

            return false; // 현금 주문임을 알림
        }
    }
