        // [전역 변수] 공휴일 데이터를 메모리에 딱 한 번만 올려두는 저장소
        // =========================================================
        public static HashSet<string> 전체공휴일셋 = new HashSet<string>();

        // [초기화] 프로그램 시작 시(또는 첫 사용 시) 딱 1번만 실행됨
        public static void 공휴일_데이터_초기화()
        {
            // 이미 데이터가 있다면 다시 할 필요 없음
            if (전체공휴일셋.Count > 0) return;

            // 2023~2030년 모든 데이터 통합
            string 전체데이터 =
                Get_2023_공휴일_데이터() + Get_2024_공휴일_데이터() +
                Get_2025_공휴일_데이터() + Get_2026_공휴일_데이터() +
                Get_2027_공휴일_데이터() + Get_2028_공휴일_데이터() +
                Get_2029_공휴일_데이터() + Get_2030_공휴일_데이터();

            // HashSet에 저장 (고속 검색용)
            string[] 날짜배열 = 전체데이터.Split(new[] { ';' }, StringSplitOptions.RemoveEmptyEntries);
            foreach (string 날짜 in 날짜배열)
            {
                전체공휴일셋.Add(날짜);
            }

            // Form1.Console_print($"[시스템] 공휴일 데이터 {전체공휴일셋.Count}일 로딩 완료.");
        }

        // =========================================================
        // [기능 1] 오늘이 휴장일인지 확인
        // =========================================================
        public static void 공휴일계산()
        {
            // 데이터가 비어있으면 초기화 먼저 수행
            if (전체공휴일셋.Count == 0) 공휴일_데이터_초기화();

            // HashSet에서 즉시 조회 (Contains 속도 매우 빠름)
            if (전체공휴일셋.Contains(DateTime.Now.ToString("yyyyMMdd")))
            {
                Form1.공휴일 = true;
            }
        }

        // =========================================================
        // [기능 2] 실제 거래일(영업일) 계산 - 최적화 적용됨
        // =========================================================
        public static string Get실제거래일계산(DateTime 시작일)
        {
            // 데이터가 비어있으면 초기화 먼저 수행 (안전장치)
            if (전체공휴일셋.Count == 0) 공휴일_데이터_초기화();

            DateTime 오늘 = DateTime.Now.Date;

            // 매수 당일(0일) 제외하고 다음 날부터 1일 카운트
            DateTime 체크일 = 시작일.Date.AddDays(1);

            int 거래일카운트 = 0;

            while (체크일 <= 오늘)
            {
                // A. 주말(토, 일) 제외
                if (체크일.DayOfWeek != DayOfWeek.Saturday && 체크일.DayOfWeek != DayOfWeek.Sunday)
                {
                    // B. [핵심] 미리 만들어둔 전역 HashSet 사용 (메모리 낭비 X, 속도 UP)
                    string 포맷날짜 = 체크일.ToString("yyyyMMdd");
                    if (!전체공휴일셋.Contains(포맷날짜))
                    {
                        거래일카운트++;
                    }
                }
                체크일 = 체크일.AddDays(1);
            }

            return 거래일카운트.ToString();
        }

